    public void Submit_Details()
    {
        try
        {
            Class.forName("org.sqlite.JDBC");
        }
        catch (Exception e)
        {   
            JOptionPane.showMessageDialog(this,e); 
        }
        
        Connection con=null;
        try
        {
            con = DriverManager.getConnection("jdbc:sqlite:Dashboard.db");
            
            String name=namefield.getText();
            String number=numberfield.getText();
            String email=emailfield.getText();
            String message=messagefield.getText();
            
            PreparedStatement query=con.prepareStatement("INSERT INTO Contacts (Name, Number, Email, Message) VALUES (?,?,?,?)");
            query.setString(1, name);
            query.setString(2, number);
            query.setString(3, email);
            query.setString(4, message);
            query.executeUpdate();
            query.close();
            con.close();
            Clear_textfields();
            
        }
        catch( SQLException s)
        {
            JOptionPane.showMessageDialog(this,s);
        }
    }
    
    public void Clear_textfields()
    {
        namefield.setText(null);
        numberfield.setText(null);
        emailfield.setText(null);
        messagefield.setText(null);
    }
    
